package Lectures;

public class TypeCasting {
    public TypeCasting() {
    }

    public static void main(String[] args) {
        float a = 20.04F;
        System.out.println((int)a);
        double b = 10.0;
        System.out.printf("%.15f \n", b);
        System.out.println((float)b);
    }
}
